 class DrinkIngredient {

     private String ingredient1;
     private String ingredient2;
     private String ingredient3;

     private double amount1;
     private double amount2;
     private double amount3;


     public DrinkIngredient(String ingredient1, String ingredient2, String ingredient3, double amount1, double amount2, double amount3) {
         this.ingredient1 = ingredient1;
         this.ingredient2 = ingredient2;
         this.ingredient3 = ingredient3;
         this.amount1 = amount1;
         this.amount2 = amount2;
         this.amount3 = amount3;
     }

     String getIngredient1() {
         return ingredient1;
     }

     String getIngredient2() {
         return ingredient2;
     }

     String getIngredient3() {
         return ingredient3;
     }

     double getAmount1() {
         return amount1;
     }

      double getAmount2() {
         return amount2;
     }

     double getAmount3() {
         return amount3;
     }

 }



















